package NodeMaterials;

public class NodeConfig {

    String nodeID;
    // node network address ( could be considered to work like localhost )
    String host;
    int port;

    public NodeConfig(String nodeID) {
        this.nodeID = nodeID;
        this.host = localhost;
        this.port = 4444;
    }

    public void NodeConfig() {

    }

    public String getNodeID() {
        return nodeID;
    }

    public String getHost() {
        return this.host;
    }

    public int getPort() {
        return port;
    }

    public void equals() {

    }



}
